// Sub-component to handle animating opacity for an individual Path
const AnimatedSvgPath = ({ d, fill, delay, progress }: { d: string; fill: string; delay: number; progress: Animated.Value }) => {
  const opacity = progress.interpolate({
    inputRange: [delay, delay + 600],
    outputRange: [0, 1],
    extrapolate: 'clamp',
  });

  return <AnimatedPath d={d} fill={fill} opacity={opacity} />;
};

export default function SplashScreen({ children }: SplashScreenProps) {
  // We drive the animation using a single master progress timer (from 0 to 4000ms)
  const progress = useRef(new Animated.Value(0)).current;

  useEffect(() => {
    Animated.timing(progress, {
      toValue: 4000,
      duration: 4000,
      useNativeDriver: true,
    }).start();
  }, []);

  return (
    <View style={styles.container}>
      {/* Centered Area for SVG or other Icons */}
      <View style={styles.iconContainer}>
        {children ? (
          children
        ) : (
          <Svg width={260} height={142} viewBox="0 0 1024 560" fill="none">
            {PATHS.map((path, index) => (
              <AnimatedSvgPath
                key={index}
                d={path.d}
                fill={path.fill}
                delay={path.delay}
                progress={progress}
              />
            ))}
          </Svg>
        )}
      </View>

      {/* Animated Demo Footer Text */}
      <Animated.Text
        style={[
          styles.footerText,
          {
            opacity: progress.interpolate({
              inputRange: [1800, 3200],
              outputRange: [0, 0.75],
              extrapolate: 'clamp',
            }),
          },
        ]}
      >
        XUnifire Demo Application
      </Animated.Text>
    </View>
  );
}

